// While Loops...
let i = 1;
while (i <= 10) {
    console.log(`Value of index is ${i}`)
    i++;
}

let myArr = ["Flash", "Superman", "batman"];

let arr = 0;
while (arr < myArr.length) {
    console.log(`Value is ${myArr[arr]}`)
    arr++;
}

// do-While Loops...
let score = 1;
do {
    console.log(`The value of score is ${score}`)
    score++;
} while (score <= 10);